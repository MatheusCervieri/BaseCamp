int	ft_is_prime(int nb)
{
	int	y;

	y = 2;
	if (nb < 2)
	{
		return (0);
	}
	while (y <= nb / 2)
	{
		if (nb % y == 0)
		{
			return (0);
		}
		y++;
	}
	return (1);
}
